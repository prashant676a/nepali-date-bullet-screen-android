    fun daysInMonth(year: Int, month: Int): Int {
        require(year in MIN_YEAR..MAX_YEAR) { "BS year $year out of range ($MIN_YEAR..$MAX_YEAR)" }
        require(month in 1..12) { "Month $month out of range (1..12)" }
        return DATA[year - MIN_YEAR][month]
    }

    fun daysInYear(year: Int): Int {
        require(year in MIN_YEAR..MAX_YEAR) { "BS year $year out of range ($MIN_YEAR..$MAX_YEAR)" }
        val months = DATA[year - MIN_YEAR]
        var total = 0
        for (m in 1..12) total += months[m]
        return total
    }
